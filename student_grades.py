df.loc[df["Average"].idxmin()]
df.loc[df["Maths"].idxmax()]

import matplotlib.pyplot as plt
df.plot(x="Name", y=["Maths","Science","English","Average"], kind="bar")
plt.show()

df.to_csv("student_scores.csv", index=False)

